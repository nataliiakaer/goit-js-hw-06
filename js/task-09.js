function getRandomHexColor() {
  return `#${Math.floor(Math.random() * 16777215)
    .toString(16)
    .padStart(6, 0)}`;
}

const bodyEl = document.querySelector("body");
const buttonEl = document.querySelector(".color");
const spanColorEl = document.querySelector(".change-color");

spanColorEl.addEventListener("click", onColor);

function onColor() {
  bodyEl.style.backgroundColor = getRandomHexColor();
  buttonEl.textContent = bodyEl.style.backgroundColor;
  console.log(buttonEl.textContent);
}
